#include <iostream>
using namespace std;
int main(){
    int a, b, c;
    // single input
    cout << "Input a Value: ";
    cin >> a;
    cout << "Value of a = " << a << "\n";
    // multiple input
    cout << "Input 2 Values: ";
    cin >> b >> c;
    cout << "Value of b = " << b << " and value of c = " << c << "\n";
    return 0;
}